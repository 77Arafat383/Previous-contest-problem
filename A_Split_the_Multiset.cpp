void solve()
{
    int n,k;
    cin>>n>>k;
    if(n==1)
    {
        cout<<"0\n";
        return;
    }
    if(n<=k)
    {
        cout<<1<<endl;
        return;
    }
    int ans=1;
    n-=k;
    k--;
    ans+=n/k;
    if(n%k!=0) ans++;
    cout<<ans<<endl;
}


